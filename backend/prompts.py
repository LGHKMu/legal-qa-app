def format_articles(chunks: list[dict]) -> str:
    lines: list[str] = []
    for i, chunk in enumerate(chunks, start=1):
        hierarchy = chunk.get("hierarchy") or ""
        header = f"[{i}] 《{chunk['law_name']}》{chunk['article_no']}"
        if hierarchy:
            header += f"（{hierarchy}）"
        lines.append(f"{header}\n{chunk['text']}")
    return "\n\n".join(lines)
